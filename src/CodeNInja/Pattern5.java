package CodeNInja;

import java.util.Scanner;

public class Pattern5 {

	public static void main(String[] args) {
		Scanner s1=new Scanner(System.in);
		int n=s1.nextInt();
		
		int i=1;
		while(i<=n) {
			int j=1;
			while(j<=n) {
				System.out.print(n);
				j++;
				
			}
			System.out.println();
			i++;
		}

	}

}
